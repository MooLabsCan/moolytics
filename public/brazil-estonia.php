<div class="lede">Two tax systems. One obeys microeconomic law &mdash; and thrives. The other defies it &mdash; and rots.</div>

<h2>Two Models, One Verdict</h2>

<h3>Brazil: Tax the Seed, Eat the Harvest</h3>
<p>Since 1996, Brazil has run a tax experiment in reverse:</p>
<ul>
  <li><strong>34% corporate tax</strong> hits profits the moment they're earned &mdash; before a single real can be reinvested.</li>
  <li><strong>0% on dividends</strong> &mdash; so the moment cash leaves the company, it's tax-free forever.</li>
</ul>
<p>Result? A system that punishes growth, rewards extraction, and starves the future. Economists don't call it "simplification" anymore. They call it <strong>economic self-sabotage</strong>.</p>

<h3>Estonia: Tax the Harvest, Let the Seed Grow</h3>
<p>In 2000, Estonia did the exact opposite:</p>
<ul>
  <li><strong>0% tax on profits</strong> &mdash; as long as they stay in the company.</li>
  <li><strong>20% (soon 22%)</strong> &mdash; only when profits are distributed as dividends.</li>
</ul>
<p>Result? A system that obeys the iron law of compounding: <strong>retain &rarr; reinvest &rarr; grow &rarr; tax later, on a bigger pie</strong>. This isn't ideology. It's microeconomics 101, proven at scale.</p>

