<h2>Conclusion</h2>
<p>Brazil's tax system &mdash; taxing profits heavily, labour relentlessly, and dividends not at all &mdash; has created a structural imbalance that puts workers last and disincentivizes innovation. The Estonian model demonstrates that it's possible to foster growth and fairness simultaneously by taxing profits <em>when</em> they're extracted, not <em>when</em> they're earned.</p>
<p>If Brazil truly seeks inclusive growth and social mobility, the answer lies not in higher taxes on the wealthy per se, but in modernizing how taxation interacts with investment and work. Growth comes not from squeezing more out of what's there &mdash; but from letting capital and labour grow together.</p>

