    <li><strong>Flat 20% tax only on distributed profits</strong> &mdash; no thresholds, no exemptions, no loopholes.</li>
    <li><strong>Sunset the 1996 dividend exemption over 36 months</strong> &mdash; grandfather existing firms, signal certainty to new ones.</li>
  </ol>
  <p>Cost to treasury in year 1: ~R$ 28 bn (0.3% of GDP). Offset within 5 years via higher GDP growth (+1.1 pp p.a., per IPEA simulation) and broader VAT base from new formal jobs.</p>
