<p>A 2023 FGV-SP panel study of 8,400 listed firms found that a 1 percentage-point increase in dividend payout ratio cuts next-year capital expenditure by 0.64 pp &mdash; a direct tax on future jobs. The numbers are merciless: <strong>slogans don't compound; retained earnings do.</strong></p>

<h2>What's Next for Brazil?</h2>
<p>Brazil's government is signalling change. A long-debated reform proposal would introduce a 10% withholding tax on dividends above a certain threshold. According to Covington &amp; Burling LLP, the measure forms part of a broader strategy to increase revenue and "rebalance" who pays. Critics argue that the reform is not about simplification or growth, but about extracting more tax from capital income without addressing the deeper inefficiencies of Brazil's tax structure.</p>

