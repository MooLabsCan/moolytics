  <p class="lede">Finland is quietly pulling off one of the most polite economic makeovers in Nordic history. Picture this: a country famous for saunas, heavy metal bands, and a welfare state so cozy it could double as a weighted blanket. Now imagine its government deciding, &ldquo;Let&rsquo;s add a little free-market spice to this mix &mdash; but keep the rye bread and personal space intact.&rdquo;</p>
